'''
7) Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3
 números.
'''

#Inicio
a = int(input("Escreva um número: "))
b = int(input("Escreva um segundo número: "))
c = int(input("Escreva um terceiro número: "))

media = (a+b+c)/3

print("A média dos números informados é: ",media)

