'''
Desenvolva um pequeno programa em python chamado primeiro.py que 
faça a leitura de 4 números e escreva na tela o somatório e a 
multiplicação dos seus valores 
'''

#Inicio
a = int(input("Escreva um número: "))
b = int(input("Escreva um segundo número: "))
c = int(input("Escreva um terceiro número: "))
d = int(input("Escreva um quarto número: "))

print("O somatório desses números é",a+b+c+d)
print("A multiplicação desses números é",a*b*c*d)

